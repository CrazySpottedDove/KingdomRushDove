#!/usr/bin/env python3
"""
Convert an image to pixel-art character map for PixelArtView.

Usage:
    python3 img2pixelart.py input.png [--width 48] [--colors 13] -o output.lua
"""

import argparse
from PIL import Image
import os

HEX_CHARS = "0123456789ABCDEF"


def main():
    parser = argparse.ArgumentParser(description="Convert image to pixel-art character map")
    parser.add_argument("input", help="Input image path")
    parser.add_argument("-o", "--output", default="output.lua", help="Output Lua file path")
    parser.add_argument("-W", "--width", type=int, default=48, help="Output width in pixels (maintains aspect ratio)")
    parser.add_argument("-C", "--colors", type=int, default=13, help="Number of palette colors (max 15)")
    parser.add_argument("--name", default="PIXEL_MAP", help="Lua variable name for the pixel map")
    args = parser.parse_args()

    n_colors = min(args.colors, 15)

    img = Image.open(args.input).convert("RGB")
    w, h = img.size
    ratio = args.width / w
    new_w = args.width
    new_h = max(1, round(h * ratio))
    img = img.resize((new_w, new_h), Image.NEAREST)

    quant = img.quantize(colors=n_colors, method=Image.Quantize.MEDIANCUT)

    palette = quant.getpalette()[:n_colors * 3]
    palette = [(palette[i], palette[i + 1], palette[i + 2]) for i in range(0, len(palette), 3)]

    indices = []
    for y in range(new_h):
        for x in range(new_w):
            indices.append(quant.getpixel((x, y)))

    pixel_map = []
    for y in range(new_h):
        row_chars = []
        for x in range(new_w):
            idx = indices[y * new_w + x]
            row_chars.append(HEX_CHARS[idx + 1])
        pixel_map.append("".join(row_chars))

    out_lines = []
    out_lines.append("-- Auto-generated by img2pixelart.py")
    out_lines.append(f"-- Source: {os.path.basename(args.input)}")
    out_lines.append("")
    out_lines.append(f"local pixel_palette = {{")
    for i, (r, g, b) in enumerate(palette):
        out_lines.append(f"\t{{{r}, {g}, {b}}},")
    out_lines.append("}")
    out_lines.append("")
    out_lines.append(f"local pixel_map = {{")
    for row in pixel_map:
        out_lines.append(f'\t"{row}",')
    out_lines.append("}")
    out_lines.append("return {pixel_palette = pixel_palette, pixel_map = pixel_map}")

    with open(args.output, "w") as f:
        f.write("\n".join(out_lines) + "\n")

    print(f"Written to {args.output}")
    print(f"  Size: {new_w}x{new_h}")
    print(f"  Colors: {len(palette)}")


if __name__ == "__main__":
    main()
